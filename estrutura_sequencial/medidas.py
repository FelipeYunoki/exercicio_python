a = float(input("Digite a medida de A: "))
b = float(input("Digite a medida de B: "))
c = float(input("Digite a medida de C: "))

quadrado = a * a
triangulo = (a * b) / 2
trapezio = ((a + b) * c) / 2

print(f"ÁREA DO QUADRADO: {quadrado:.4f}")
print(f"ÁREA DO TRIÂNGULO: {triangulo:.4f}")
print(f"ÁREA DO TRAPÉZIO: {trapezio:.4f}")